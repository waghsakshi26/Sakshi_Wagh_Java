class Floatvalue{
	public static void main(String[]args){
		float num=120f;
		float num1=89.96f;
	if(num%6==0){
	   System.out.println("120 is divisible by 6");
	}
        else{
	  System.out.println("120 is not divisible by 6");
        }
        if(num1%6==0){
	   System.out.println("89.96 is divisible by 6");
	}
        else{
	   System.out.println("89.96 is not divisible by 6");
	}
	}
}	
