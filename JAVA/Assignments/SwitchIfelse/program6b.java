class SwitchDemo1{
	public static void main(String[]args){
		int num=6;
		switch(num){
		 case 1:
			System.out.println("1 is greater than 5");
			break;
		case 2:
			System.out.println("2 is greater than 5");
			break;
		case 3:
			 System.out.println("3 is greater than 5");
			 break;
                case 4:
			  System.out.println("4 is greater than 5");
			  break;
                case 5:
			   System.out.println("5 is greater than 5");
			   break;
                case 6:
			   System.out.println("6 is greater than 5");
			   break;
                default:
			   System.out.println("In default state");
		}
	}
}
