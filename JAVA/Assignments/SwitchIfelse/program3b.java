class SwitchDemo{
	public static void main(String[]args){
		char data ='S';
		switch (data){
			case 'S':
				System.out.println("Small");
				break;
			case 'X':
				System.out.println("Extra Large");
				break;
			default :
				System.out.println("In Default state");
		}
	}
}




