class SwitchDemo{
	public static void main(String[]args){
		int x=5;
		switch(x){
			case 5:
				System.out.println("Friday");
				break;
			case 6:
				System.out.println("Sunday");
				break;
			default:
				System.out.println("default state");
		}
	}
}
