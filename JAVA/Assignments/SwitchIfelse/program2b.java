class Switchcase1{
	public static void main(String[]args){
		char data='O';
		switch(data){
	        case 'O':
			System.out.println("Outstanding");
			break;
		case 'A':
			System.out.println("Excellent");
			break;
		default :
			System.out.println("In default State");
		}
	}
}



